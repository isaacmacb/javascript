//Objetos são coleções de propriedades (chaves) e valores. Eles podem armazenar qualquer tipo de dados e são úteis para organizar dados relacionados.
// chave (propriedades) = {}
// valores = são os valores associados a essas chaves. Podem ser de qualquer tipo de dado (string, número, booleano, outro objeto, array, função, etc.).

//declarando um objeto
const pessoa = {
    nome : "Isaac",
    idade : 12,
    profissão: "desenvolvedor"
}

//pessoa é o objeto, com 3 propriedades (nome, idade e profissão) e 3 valores de cada propriedade

console.log(pessoa.idade);
console.log(pessoa.nome);
console.log(pessoa.profissão);


