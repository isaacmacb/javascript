//O for...of é utilizado para iterar sobre elementos de uma coleção iterável (como arrays, strings, maps, sets, etc.).

let frutas = ['maçã', 'banana', 'laranja'];

for (let fruta of frutas) { //percorrendo dentro da array, logando todas as frutas
  console.log(fruta); // Imprime maçã, banana, laranja
}
